#Some_Counting
#Write a loop that counts up from 0 to 50 in increments of 1.

for i in range(51):    #0 to 50
    print(i)

#Write a loop that counts down from 50 to 0 in decrements of 1.
for x in range(50,-1,-1):  #50 is start, -1 is limit to end the count in 0, -1 to count down(skip value)
    print(x)
    x-=1

#Write a loop that counts up from 30 to 50 in increments of 1.
for a in range(30,51,1):     #30 is starting, 51 is the limit to end the loop in 50, increment of 1
    print(a)
    

#Write a loop that counts down from 50 to 10 in decrements of 2.
for z in range(50,8,-2):     #50 is start, 8 is limit to end the count in 10, decrement of 2
    print(z)


#Write a loop that counts up from 100 to 200 in increments of 5. 
for y in range(100,205,5):    #100 start, 205 limit, increment of 5
    print(y)
  
